#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	long long	n2;

	n2 = n;
	if (n2 < 0)
	{
		write(fd, "-", 1);
		n2 = -n2;
	}
	if (n2 > 9)
	{
		ft_putnbr_fd(n2 / 10, fd);
	}
	c = ('0' + (n2 % 10));
	write(fd, &c, 1);
}
